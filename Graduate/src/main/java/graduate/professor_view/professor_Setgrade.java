    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        login_log a = new login_log();
        a.Read_log();
        String year = jComboBox1.getSelectedItem().toString();
        String semester = jComboBox2.getSelectedItem().toString();
        String Class = a.login_log[4];
        String Classname = jComboBox3.getSelectedItem().toString();
        
        DefaultTableModel model = (DefaultTableModel)jTable1.getModel();
        String ob = "학번;이름;학점;";
         
         for(int m=0;m<3;m++)
            { 
               String[] str = ob.split(";");
               model.setColumnIdentifiers(str); 
            }
         
         try{
               File file = new File("C:\\DB\\Class\\"+Class+" "+year+"년도"+" "+semester+"\\"+Classname+".txt");
               FileReader filereader = new FileReader(file);
               try (BufferedReader bufReader = new BufferedReader(filereader)) {
                    model.setNumRows(0);
                    Object[] tableLines = bufReader.lines().toArray(); 
                    for(int i=0; i< tableLines.length;i++){
                        String line=tableLines[i].toString().trim();
                        String[] dataRow = line.split("\t");
                        model.addRow(dataRow);
                    }
                    filereader.close();
                  bufReader.close();
                }
            }catch (FileNotFoundException e){
                System.out.println(e);
            }catch(IOException e){
                System.out.println(e);
            }
         
        
    }//GEN-LAST:event_jButton1ActionPerformed

