    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        login_log a = new login_log();
        a.Read_log();
        String year = jComboBox1.getSelectedItem().toString();
        String semester = jComboBox2.getSelectedItem().toString();
        String Class = a.login_log[4];
        String Classname = jComboBox3.getSelectedItem().toString();
        
        try{
            File file = new File("C:\\DB\\Class\\"+Class+" "+year+"년도"+" "+semester+"\\"+Classname+".txt");
            if(!file.exists()){
                file.createNewFile();
            }
            FileWriter fw = new FileWriter(file.getAbsoluteFile());
            BufferedWriter bw = new BufferedWriter(fw);

            for(int i = 0; i<jTable1.getRowCount();i++){
                for(int j = 0;j<jTable1.getColumnCount();j++){
                    bw.write(jTable1.getModel().getValueAt(i, j)+"\t");
                }
                bw.write("\n");
            }
            bw.close();
            fw.close(); 
            JOptionPane.showMessageDialog(null, "저장되었습니다.");
        }catch(IOException ex){
            System.out.println(ex);
        }
    }//GEN-LAST:event_jButton4ActionPerformed

    private void UpdateCombo(){
        login_log a = new login_log();
        a.Read_log();
        jComboBox3.removeAllItems();
        jComboBox3.addItem("----");
        String year = jComboBox1.getSelectedItem().toString();
        String semester = jComboBox2.getSelectedItem().toString();
        String Class = a.login_log[4];
        String myname = a.login_log[2];
        
        try{
               File file = new File("C:\\DB\\Class\\"+Class+" "+year+"년도"+" "+semester+"\\allclass.txt");
               if(!file.exists()){
                   JOptionPane.showMessageDialog(null, "담당한 교과목이 없습니다.");
               }
               FileReader filereader = new FileReader(file);
               try (BufferedReader bufReader = new BufferedReader(filereader)) {
                    Object[] tableLines = bufReader.lines().toArray(); 
                    for(int i=0; i< tableLines.length;i++){
                        String line=tableLines[i].toString().trim();
                        if(line.contains(myname)){
                            String[] dataRow = line.split("\t");
                            jComboBox3.insertItemAt(dataRow[1], jComboBox3.getItemCount());       
                        }
                    }
                    filereader.close();
                  bufReader.close();
                }
            }catch (FileNotFoundException e){
                System.out.println(e);
            }catch(IOException e){
                System.out.println(e);
            }
        
        
    }
