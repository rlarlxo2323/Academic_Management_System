    private void ReadTable(){
        try{
               File file = new File("C:\\DB\\Class\\AllClass.txt");
               FileReader filereader = new FileReader(file);
               try (BufferedReader bufReader = new BufferedReader(filereader)) {
                    String firstline = bufReader.readLine().trim();
                    String[] columsName = firstline.split("\t");
                    DefaultTableModel model = (DefaultTableModel)jTable1.getModel();
                    model.setNumRows(0);
                    model.setColumnIdentifiers(columsName);  
                    Object[] tableLines = bufReader.lines().toArray(); 
                    for(int i=0; i< tableLines.length;i++){
                        String line=tableLines[i].toString().trim();
                        String[] dataRow = line.split("\t");
                        model.addRow(dataRow);
                    }
                    
                    filereader.close();
                  bufReader.close();
                }
            }catch (FileNotFoundException e){
                System.out.println(e);
            }catch(IOException e){
                System.out.println(e);
            }
         jTable1.getColumnModel().getColumn(0).setMaxWidth(20);
    }
    
    public void SaveFile(){
        DefaultTableModel model = (DefaultTableModel)jTable1.getModel();
            try{
            File file = new File("C:\\DB\\Class\\AllClass.txt");
            if(!file.exists()){
                file.createNewFile();
            }
            FileWriter fw = new FileWriter(file.getAbsoluteFile());
            BufferedWriter bw = new BufferedWriter(fw);
            String ob;
            for(int m=0;m<jTable1.getColumnCount();m++)
            { 
               ob = model.getColumnName(m);
               bw.write(ob+"\t");  
            }
            bw.write("\n");
            for(int i = 0; i<jTable1.getRowCount();i++){
                for(int j = 0;j<jTable1.getColumnCount();j++){
                    bw.write(jTable1.getModel().getValueAt(i, j)+"\t");
                }
                bw.write("\n");
            }
            bw.close();
            fw.close(); 
            ClassCode.setText("");
            ClassNum_Field.setText("");
            ClassInfo_Field.setText("");
            ClassBox_Box.setSelectedItem("선택 없음");
            ClassSize_Box.setSelectedItem("---");
            ClassName_Field.setText("");
        }catch(IOException ex){
            System.out.println(ex);
        }
    }
    

    
