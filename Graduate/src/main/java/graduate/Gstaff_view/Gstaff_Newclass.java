    private void NewClass_ButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_NewClass_ButtonActionPerformed
        Object[] row = new Object[6];    
            if(ClassBox_Box.getSelectedItem().equals("선택 없음")){
                JOptionPane.showMessageDialog(null, "학과를 선택하세요.");    
            }
            else if(ClassNum_Field.getText().equals("")){
                JOptionPane.showMessageDialog(null, "강좌번호를 입력하세요.");     
            }
            else if(ClassSize_Box.getSelectedItem().equals("---")){
                JOptionPane.showMessageDialog(null, "학점 수를 선택하세요.");
            }
            else if(ClassName_Field.getText().equals("")){
                JOptionPane.showMessageDialog(null, "강좌이름을 입력하세요.");   
            }
            else if(ClassInfo_Field.getText().isEmpty()){
                JOptionPane.showMessageDialog(null, "강좌설명을 작성하세요."); 
            }
            else{
                DefaultTableModel model = (DefaultTableModel)jTable1.getModel();
                boolean result = true;
                for(int i=0;i<model.getRowCount();i++){
                   String a = model.getValueAt(i, 2).toString();
                   String b = model.getValueAt(i, 3).toString();
                   if(a.equals(ClassCode.getText()+ClassNum_Field.getText())){
                    JOptionPane.showMessageDialog(null, "이미 존재하는 강좌번호입니다. \n 새로운 번호를 부여하세요.");
                     result = false;
                     break;
                   }
                   else if(a.contains(ClassCode.getText())){
                     if(ClassName_Field.getText().equals(b)){
                         JOptionPane.showMessageDialog(null, "이미 존재하는 강좌입니다.");
                         result = false;
                         break;
                     }
                   }
                    
                
                } 
                
                if(result){
                row[0] = "X";
                row[1] = ClassBox_Box.getSelectedItem();
                row[2] = ClassCode.getText()+ClassNum_Field.getText();
                row[3] = ClassName_Field.getText();
                row[4] = ClassSize_Box.getSelectedItem();
                row[5] = ClassInfo_Field.getText();
            model.addRow(row);
            ClassCode.setText("");
            ClassNum_Field.setText("");
            ClassInfo_Field.setText("");
            ClassBox_Box.setSelectedItem("선택 없음");
            ClassSize_Box.setSelectedItem("---");
            ClassName_Field.setText("");
            SaveFile();
            }
            }
            
    }//GEN-LAST:event_NewClass_ButtonActionPerformed

